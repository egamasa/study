# ==数値==

# 正の整数
10
# 小数
1.5
# 負の整数
-3
# 負の小数
-4.75

# 大きな数の桁区切り
p 1_000_000_000  # => 1000000000

# 四則演算
p  10 + 20  # => 30
p 100 - 25  # => 75
p  12 *  5  # => 60
p  20 /  5  # => 4

# 正負の反転
n = 1
p -n  # => -1

# 整数同士の除算で小数点以下の値を求める
p 1 / 2  # => 0 ←×
p 1.0 / 2  # => 0.5
p 1 / 2.0  # => 0.5
p 1 / 3.0  # => 0.3333333333333333

# 整数→小数
n = 1
p n.to_f  # => 1.0
p n.to_f / 2  # => 0.5

# 余り
p 8 % 3  # => 2

# べき乗
p 2 ** 3  # => 8
