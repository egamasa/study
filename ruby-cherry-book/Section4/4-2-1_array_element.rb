### 要素の変更・追加・削除 ###

# 要素の変更
a = [1, 2, 3]
a[1] = 20
p a
#=> [1, 20, 3]

# 間の要素はnilで埋められる
a = [1, 2, 3]
a[4] = 50
p a
#=> [1, 2, 3, nil, 50]

# 配列の最後に要素を追加
a = []
a << 1
a << 2
a << 3
p a
#=> [1, 2, 3]

# 要素の削除
a = [1, 2, 3]
# 削除した要素が返る
p a.delete_at(1)  #=> 2
p a  #=> [1, 3]

# 存在しない要素を指定するとnilが返る
p a.delete_at(100)  #=> nil
p a  #=> [1, 3]
