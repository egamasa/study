### 値が連続する配列を作成する ###

# Rangeオブジェクトに対して to_a メソッドを呼び出す
p (1..5).to_a   #=> [1, 2, 3, 4, 5]
p (1...5).to_a  #=> [1, 2, 3, 4]

p ('a'..'e').to_a   #=> ["a", "b", "c", "d", "e"]
p ('a'...'e').to_a  #=> ["a", "b", "c", "d"]

p ('bad'..'bag').to_a   #=> ["bad", "bae", "baf", "bag"]
p ('bad'...'bag').to_a  #=> ["bad", "bae", "baf"]


# splat展開
p [*1..5]   #=> [1, 2, 3, 4, 5]
p [*1...5]  #=> [1, 2, 3, 4]
