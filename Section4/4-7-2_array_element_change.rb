### 配列の要素のさまざまな変更方法 ###


a = [1, 2, 3]
# 添字で変更する要素を指定
a[-2] = -10
p a  #=> [1, -10, 3]

a = [1, 2, 3, 4, 5]
# 開始位置と長さを指定
a[1, 3] = 100
p a  #=> [1, 100, 5]

a = []
# pushメソッドで要素の追加
a.push(1)
p a  #=> [1]
a.push(2, 3)
p a  #=> [1, 2, 3]

a = [1, 2, 3, 1, 3, 2]
# 要素の値を指定して削除…削除した値が戻り値になる
p a.delete(2)  #=> 2
p a  #=> [1, 3, 1, 3]

# 存在しない値を指定すると戻り値はnil
p a.delete(5)  #=> nil
p a  #=> [1, 3, 1, 3]
