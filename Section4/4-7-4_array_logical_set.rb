### 配列の和集合、差集合、積集合 ###

a = [1, 2, 3]
b = [3, 4, 5]

# | :和集合
p a | b  #=> [1, 2, 3, 4, 5]

# - :差集合
p a - b  #=> [1, 2]

# & :積集合
p a & b  #=> [3]


# Setクラスを利用
require 'set'

a = Set.new([1, 2, 3])
b = Set.new([3, 4, 5])

p a | b  #=> #<Set: {1, 2, 3, 4, 5}>
p a - b  #=> #<Set: {1, 2}>
p a & b  #=> #<Set: {3}>
